public class ReportMenuModel{
    private String strClass;
    private String intOrder;
    private String strContentName;
    private String strTitle;
    private Integer image;

    public String getStrClass() {
        return strClass;
    }

    public void setStrClass(String strClass) {
        this.strClass = strClass;
    }

    public String getIntOrder() {
        return intOrder;
    }

    public void setIntOrder(String intOrder) {
        this.intOrder = intOrder;
    }

    public String getStrContentName() {
        return strContentName;
    }

    public void setStrContentName(String strContentName) {
        this.strContentName = strContentName;
    }

    public String getStrTitle() {
        return strTitle;
    }

    public void setStrTitle(String strTitle) {
        this.strTitle = strTitle;
    }

    public Integer getImage() {
        return image;
    }

    public void setImage(Integer image) {
        this.image = image;
    }
}
